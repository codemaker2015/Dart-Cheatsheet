// Set | Part 1

void main() {
  // Set is a list kind of
  var countries = {'India', 'UK', 'US'};

  // Add one
  countries.add('Australia');

  // Remove
  countries.remove('UK');

  // Get first
  print(countries.first);

  // Get last
  print(countries.last);

  // Get lenght
  print(countries.length);

  // Get data with index
  print(countries.elementAt(2));
}
