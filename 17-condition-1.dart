// Conditions | If, Else if, Else

void main() {
  const age = 15;

  if (age < 18) {
    print("You are under 18");
    print("Too young to work here");
  } else if (age < 30) {
    print("You are between 30 and 20");
    print("You can work here");
  } else {
    print("Sorry, You are too old");
    print("You can not work here");
  }
  print("Have a good day");
}
