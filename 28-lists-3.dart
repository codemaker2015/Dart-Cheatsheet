// Lists | Part 3

void main() {
  // When we use var, we can add all data types
  var names = ["Amal", "Vishnu", "Ajin", "Sarath", "Ajith"];

  // Also we can create the list with List
  List apple = ['iPhone', 'iMac', 'iCloud'];

  // Limit the list to contain Strings
  List<String> cities = ['Muvattupuzha', 'Kothamangalam', 'Kochi'];

  // We can limit items type also with var
  var windows = <String>['Windows XP', 'Windows Vista', 'Windows 8', 'Windows 10', 'Windows 11'];

  // Remember
  // We use <> to add spesefic data type
}
