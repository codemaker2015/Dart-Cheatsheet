// Conditions | Switch

void main() {
  const stage = 1;

  switch (stage) {
    case 1:
      print("Gold Medal");
      break;
    case 2:
      print("Silver Medal");
      break;
    case 3:
      print("Bronze Medal");
      break;
    default:
      print("Sorry, You are not in first 3 :(");
      break;
  }
}
